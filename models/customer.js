const mongoose = require("mongoose");
const CustomerSchema = new mongoose.Schema({
	uid:{
		type:String,
	},
	firstName:{
		type:String,
		required:true
	},
	lastName:{
		type:String,
		required:true
	},
	email:{
		type:String,
		required:true
	},
	address:{
		type:String,
		required:true
	},
	state:{
		type:String,
		required:true
	},
	country:{
		type:String,
		required:true
	},
	pin:{
		type:String,
		required:true
	},
	mobile:{
		type:String,
		required:true
	},
	gender:{
		type:String,
		required:true
	},
	dob:{
		type:Date,
		required:true
	},
	tags:{
		type:String,
		required:false
	},
	dateAdded:{
		type:Date,
		default:Date.now
	},
	source:{
		type:String,
		required:true
	},
	emc:{
		type:String,
		required:true
	},
	smc:{
		type:String,
		required:true
	},
	
});
module.exports = mongoose.model("Customer",CustomerSchema);
